<h1 class="py-4">Migration</h1>

<p>
	Le projet sanipasse a été un beau succès, et a été de loin le logiciel libre d'analyse de passes
	sanitaires et de bornes de validation le plus populaire.
</p>

<p>
	Aujourd'hui, en 2023, son utilisation a beaucoup baissé, et c'est tant mieux! Personne ne regrette
	les années COVID.
</p>

<p>
	Cependant, le nom de domaine <code>sanipasse.fr</code> a un coût. Ce n'est pas très cher (1€ par mois),
	mais étant donnée l'utilisation actuelle du site, je pense qu'il n'est plus crucial pour sanipasse
	d'avoir son propre nom de domaine.
</p>

<p>
	Je compte donc migrer sanipasse vers <a href="https://sanipasse.ophir.dev"
		><code>sanipasse.ophir.dev</code></a
	>
	(un sous-domaine de mon nom de domaine personnel, que je continuerai à payer).
</p>

<p>
	Si vous voulez conserver vos données enregistrées sur <code>sanipasse.fr</code>
	(passes enrégistrés dans l'espace "Mon Carnet", et configurations de bornes enregistrées localement),
	<strong>il vous faudra les ré-importer</strong> sur
	<a href="https://sanipasse.ophir.dev"><code>sanipasse.ophir.dev</code></a>.
</p>

<p>La migration se passera en 3 temps:</p>
<ol>
	<li>
		À partir du 21 janvier 2023, un message expliquant la migration sera affiché sur sanipasse.fr
	</li>
	<li>
		À partir du <em>1er mars 2023</em>, les requêtes vers sanipasse.fr redirigeront vers <code
			>sanipasse.ophir.dev</code
		>.
	</li>
    <li>À partir du <em>10 mai 2023</em>, <code>sanipasse.fr</code> ne sera plus disponible, et sanipasse ne sera plus accessible qu'à travers
        <a href="https://sanipasse.ophir.dev"><code>sanipasse.ophir.dev</code></li>
</ol>

<p>
	Si vous souhaitez reprendre à vos frais le projet sanipasse et le nom de domaine <code
		>sanipasse.fr</code
	>, vous pouvez toujours me contacter sur
	<a href="mailto:contact@sanipasse.fr">contat@sanipasse.fr</a>.
</p>
